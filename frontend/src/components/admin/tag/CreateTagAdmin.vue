  </nav>

  <div class="list">
    <div class="top-list">
      <h2 class="mb-4">Tags</h2>
    </div>

    <div v-if="isLoading" class="d-flex justify-content-center align-items-center vh-100">
      <div class="spinner-border text-primary" role="status">
        <span class="visually-hidden">Carregando...</span>
      </div>
    </div>

    <div class="form-area table table-hover">
      <form @submit.prevent="submitForm">
        <div class="mb-3">
          <label for="name" class="form-label">Nome</label>
          <input
            type="text"
            id="name"
            v-model="formData.name"
            class="form-control"
            required
          />
        </div>

        <button type="submit" class="btn btn-primary">Cadastrar</button>
      </form>
    </div>
  </div>
</template>

<script>
import TagAdminService from "@/service/admin/TagAdminService";

export default {
  name: "CreateTagAdmin",
  components: {},
  data() {
    return {
      formData: {
        name: '',
      },
      isLoading: false,
    };
  },
  created() {
    this.TagAdminService = new TagAdminService();
  },
  // mounted() {
  //   this.getTag();
  // },
  methods: {
    async submitForm() {
      //console.log("Dados do formul√°rio:", this.formData);

      // const data = {
      //   name: this.formData.name,
      // };
      //console.log(data);
      this.createTag(this.formData);
    },

    async createTag(data) {
      try {
        this.isLoading = true;
        const response = await this.TagAdminService.createTag(data);

        if (response) {
          this.isLoading = false;
        }
      } catch (error) {
        console.error("Erro ao salvar tag:", error.message);
      } finally {
        this.isLoading = false;
      }
    },
  },
};
</script>

